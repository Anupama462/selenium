package MavenFramework.SeleniumTesting;

public class Demo11Forloop {

	public static void main(String[] args) {
		
		int i=1;
		
		for(;i<=10;)
		{
			System.out.println(i);
			i++;
		}
		
	//	While loop - dont know the number of iterations
	//	do while - dont know the number of iterations - want to execute the code at least once irrespective of
	//	what ever the condition is
		
	//	For loop - how many iterations you want to run

	}

}
